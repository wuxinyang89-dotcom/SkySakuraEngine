#pragma once

// ============================================================
// 反射宏定义
// 这些宏本身不产生代码，仅作为标记供 ReflectionParser 识别解析
// 解析器扫描到这些宏后，会为对应的类/属性/函数生成反射信息
// ============================================================

// 标记一个类需要生成反射信息
// 用法: SCLASS() 放在 class 声明上方
#define SCLASS()

// 标记一个属性需要反射
// 用法: SPROPERTY() 放在成员变量上方
#define SPROPERTY()

// 标记一个函数需要反射
// 用法: SFUNCTION() 放在成员函数上方
#define SFUNCTION()

// 生成体宏 - 由解析器在生成文件中展开为实际反射代码
// 用法: GENERATED_BODY() 放在类体内部的第一行
#define GENERATED_BODY()
